<h2>How NeoStrata Enlighten Pigment Controller works — the science</h2>
<p>The Pigment Controller uses a multi-target approach to hyperpigmentation — addressing the problem at three different points in the melanin production cycle. Kojic acid and niacinamide inhibit tyrosinase at different molecular pathways, meaning pigmentation cannot easily bypass the treatment by routing through an uninhibited mechanism. Tranexamic acid, a more recent addition to brightening technology, interrupts the UV-triggered communication between keratinocytes and melanocytes that drives reactive pigmentation — particularly relevant for melasma.</p>
<p>Gluconolactone (PHA) exfoliates the surface simultaneously, removing the uppermost pigmented cells and accelerating the turnover that brings newer, less pigmented cells to the surface. This is not just a suppressor of new pigment production — it is actively clearing existing pigmentation from the skin surface while preventing more from forming underneath.</p>
<p>This multi-mechanism approach is why the Pigment Controller works when single-active brightening products have failed. Melanin production can route around a single enzyme inhibitor. It cannot route around three simultaneously.</p>

